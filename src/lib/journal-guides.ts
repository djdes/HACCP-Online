const FALLBACK: JournalGuide = {
  title: "Общий журнал",
  purpose: "Запись установленной формы.",
  frequency: "По плану.",
  fields: [],
  checks: ["Все обязательные поля заполнены.", "Данные внесены текущей датой."],
  redFlags: ["Поля пустые или явно скопированы из вчера."],
};

export function getJournalGuide(journalCode: string): JournalGuide {
  return GUIDES[journalCode] ?? { ...FALLBACK, title: journalCode };
}

export function hasJournalGuide(journalCode: string): boolean {
  return journalCode in GUIDES;
}

/**
 * Compact версия гайда для Telegram-сообщений (HTML-safe).
 */
export function formatGuideForTelegram(journalCode: string): string {
  const g = getJournalGuide(journalCode);
  const lines = [
    `📖 <b>${g.title}</b>`,
    "",
    `🎯 <b>Цель:</b> ${g.purpose}`,
    "",
    `🕐 <b>Когда:</b> ${g.frequency}`,
  ];
  if (g.fields.length > 0) {
    lines.push("", "📝 <b>Поля:</b>");
    for (const f of g.fields) {
      lines.push(`• <b>${f.name}</b> — ${f.description}`);
      if (f.norm) lines.push(`   <i>Норма: ${f.norm}</i>`);
    }
  }
  if (g.checks.length > 0) {
    lines.push("", "✅ <b>Что проверять:</b>");
    for (const c of g.checks) lines.push(`• ${c}`);
  }
  if (g.redFlags.length > 0) {
    lines.push("", "🚩 <b>Красные флаги:</b>");
    for (const r of g.redFlags) lines.push(`• ${r}`);
  }
  if (g.normRef) lines.push("", `📚 ${g.normRef}`);
  return lines.join("\n");
}
